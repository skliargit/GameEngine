/*
    @brief Коды кнопок мыши.
    @note Поддерживаются основные и дополнительные кнопки мыши.
*/
typedef enum mouse_button {
    BTN_UNKNOWN      = 0,         // Неизвестаня кнопка мыши.

    // --------------- Основные кнопки -----------------
    BTN_LEFT,                     // Левая кнопка мыши.
    BTN_RIGHT,                    // Правая кнопка мыши.
    BTN_MIDDLE,                   // Средняя кнопка мыши (колесо).

    // ------------ Дополнительные кнопки --------------
    BTN_FORWARD,                  // Дополнительная кнопка "вперед".
    BTN_BACKWARD,                 // Дополнительная кнопка "назад".

    BTN_COUNT,                    // Максимальное количество поддерживаемых кодов кнопок (не является реальным кодом).
} mouse_button;

/*
    @brief Коды клавиш клавиатуры.
    @note Основано на виртуальных кодах клавиш Windows.
          Microsoft: https://learn.microsoft.com/ru-ru/windows/win32/inputdev/virtual-key-codes
          Доп. сайт: https://snipp.ru/handbk/vk-code
*/
typedef enum keyboard_key {
    KEY_UNKNOWN      = 0,         // Неизвестаня клавиша.

    // ------------- Специальные клавиши ---------------
    KEY_BACKSPACE    = 0x08,      // Клавиша Backspace.
    KEY_TAB          = 0x09,      // Клавиша Tab.
    KEY_RETURN       = 0x0D,      // Клавиша Enter.
    KEY_PAUSE        = 0x13,      // Клавиша Pause.
    KEY_CAPSLOCK     = 0x14,      // Клавиша Caps Lock.
    KEY_ESCAPE       = 0x1B,      // Клавиша Escape.
    KEY_SPACE        = 0x20,      // Клавиша Пробел.
    KEY_PAGEUP       = 0x21,      // Клавиша Page Up.
    KEY_PAGEDOWN     = 0x22,      // Клавиша Page Down.
    KEY_END          = 0x23,      // Клавиша End.
    KEY_HOME         = 0x24,      // Клавиша Home.

    // --------- Клавиши управления курсором -----------
    KEY_LEFT         = 0x25,      // Стрелка влево.
    KEY_UP           = 0x26,      // Стрелка вверх.
    KEY_RIGHT        = 0x27,      // Стрелка вправо.
    KEY_DOWN         = 0x28,      // Стрелка вниз.

    // ----- Дополнительные специальные клавиши --------
    KEY_PRINTSCREEN  = 0x2C,      // Клавиша Print Screen.
    KEY_INSERT       = 0x2D,      // Клавиша Insert.
    KEY_DELETE       = 0x2E,      // Клавиша Delete.

    // -------------- Цифровые клавиши -----------------
    KEY_0            = 0x30,      // Цифра 0.
    KEY_1            = 0x31,      // Цифра 1.
    KEY_2            = 0x32,      // Цифра 2.
    KEY_3            = 0x33,      // Цифра 3.
    KEY_4            = 0x34,      // Цифра 4.
    KEY_5            = 0x35,      // Цифра 5.
    KEY_6            = 0x36,      // Цифра 6.
    KEY_7            = 0x37,      // Цифра 7.
    KEY_8            = 0x38,      // Цифра 8.
    KEY_9            = 0x39,      // Цифра 9.

    // ------------- Буквенные клавиши -----------------
    KEY_A            = 0x41,      // Клавиша A.
    KEY_B            = 0x42,      // Клавиша B.
    KEY_C            = 0x43,      // Клавиша C.
    KEY_D            = 0x44,      // Клавиша D.
    KEY_E            = 0x45,      // Клавиша E.
    KEY_F            = 0x46,      // Клавиша F.
    KEY_G            = 0x47,      // Клавиша G.
    KEY_H            = 0x48,      // Клавиша H.
    KEY_I            = 0x49,      // Клавиша I.
    KEY_J            = 0x4A,      // Клавиша J.
    KEY_K            = 0x4B,      // Клавиша K.
    KEY_L            = 0x4C,      // Клавиша L.
    KEY_M            = 0x4D,      // Клавиша M.
    KEY_N            = 0x4E,      // Клавиша N.
    KEY_O            = 0x4F,      // Клавиша O.
    KEY_P            = 0x50,      // Клавиша P.
    KEY_Q            = 0x51,      // Клавиша Q.
    KEY_R            = 0x52,      // Клавиша R.
    KEY_S            = 0x53,      // Клавиша S.
    KEY_T            = 0x54,      // Клавиша T.
    KEY_U            = 0x55,      // Клавиша U.
    KEY_V            = 0x56,      // Клавиша V.
    KEY_W            = 0x57,      // Клавиша W.
    KEY_X            = 0x58,      // Клавиша X.
    KEY_Y            = 0x59,      // Клавиша Y.
    KEY_Z            = 0x5A,      // Клавиша Z.

    // ------------- Системные клавиши -----------------
    KEY_LSUPER       = 0x5B,      // Левая клавиша Windows/Super.
    KEY_RSUPER       = 0x5C,      // Правая клавиша Windows/Super.
    KEY_MENU         = 0x5D,      // Клавиша контекстного меню.
    KEY_SLEEP        = 0x5F,      // Клавиша Sleep.

    // --------------- Цифровой блок -------------------
    KEY_NUMPAD0      = 0x60,      // Цифра 0 на цифровом блоке.
    KEY_NUMPAD1      = 0x61,      // Цифра 1 на цифровом блоке.
    KEY_NUMPAD2      = 0x62,      // Цифра 2 на цифровом блоке.
    KEY_NUMPAD3      = 0x63,      // Цифра 3 на цифровом блоке.
    KEY_NUMPAD4      = 0x64,      // Цифра 4 на цифровом блоке.
    KEY_NUMPAD5      = 0x65,      // Цифра 5 на цифровом блоке.
    KEY_NUMPAD6      = 0x66,      // Цифра 6 на цифровом блоке.
    KEY_NUMPAD7      = 0x67,      // Цифра 7 на цифровом блоке.
    KEY_NUMPAD8      = 0x68,      // Цифра 8 на цифровом блоке.
    KEY_NUMPAD9      = 0x69,      // Цифра 9 на цифровом блоке.
    KEY_MULTIPLY     = 0x6A,      // Клавиша умножения (*).
    KEY_ADD          = 0x6B,      // Клавиша сложения (+).
    KEY_SUBTRACT     = 0x6D,      // Клавиша вычитания (-).
    KEY_DECIMAL      = 0x6E,      // Клавиша десятичной точки (.).
    KEY_DIVIDE       = 0x6F,      // Клавиша деления (/).

    // ---------- Функциональные клавиши ---------------
    KEY_F1           = 0x70,      // Функциональная клавиша F1.
    KEY_F2           = 0x71,      // Функциональная клавиша F2.
    KEY_F3           = 0x72,      // Функциональная клавиша F3.
    KEY_F4           = 0x73,      // Функциональная клавиша F4.
    KEY_F5           = 0x74,      // Функциональная клавиша F5.
    KEY_F6           = 0x75,      // Функциональная клавиша F6.
    KEY_F7           = 0x76,      // Функциональная клавиша F7.
    KEY_F8           = 0x77,      // Функциональная клавиша F8.
    KEY_F9           = 0x78,      // Функциональная клавиша F9.
    KEY_F10          = 0x79,      // Функциональная клавиша F10.
    KEY_F11          = 0x7A,      // Функциональная клавиша F11.
    KEY_F12          = 0x7B,      // Функциональная клавиша F12.
    KEY_F13          = 0x7C,      // Функциональная клавиша F13.
    KEY_F14          = 0x7D,      // Функциональная клавиша F14.
    KEY_F15          = 0x7E,      // Функциональная клавиша F15.
    KEY_F16          = 0x7F,      // Функциональная клавиша F16.
    KEY_F17          = 0x80,      // Функциональная клавиша F17.
    KEY_F18          = 0x81,      // Функциональная клавиша F18.
    KEY_F19          = 0x82,      // Функциональная клавиша F19.
    KEY_F20          = 0x83,      // Функциональная клавиша F20.
    KEY_F21          = 0x84,      // Функциональная клавиша F21.
    KEY_F22          = 0x85,      // Функциональная клавиша F22.
    KEY_F23          = 0x86,      // Функциональная клавиша F23.
    KEY_F24          = 0x87,      // Функциональная клавиша F24.

    // ------- Клавиши переключения режимов ------------
    KEY_NUMLOCK      = 0x90,      // Клавиша Num Lock.
    KEY_SCROLLLOCK   = 0x91,      // Клавиша Scroll Lock.

    // -------- Модификаторы (левые/правые) ------------
    KEY_LSHIFT       = 0xA0,      // Левая клавиша Shift.
    KEY_RSHIFT       = 0xA1,      // Правая клавиша Shift.
    KEY_LCONTROL     = 0xA2,      // Левая клавиша Control.
    KEY_RCONTROL     = 0xA3,      // Правая клавиша Control.
    KEY_LALT         = 0xA4,      // Левая клавиша Alt.
    KEY_RALT         = 0xA5,      // Правая клавиша Alt.

    // ------------ Символьные клавиши -----------------
    KEY_SEMICOLON    = 0xBA,      // Клавиша точки с запятой (;).
    KEY_QUOTE        = 0xDE,      // Клавиша кавычки (").
    KEY_APOSTROPHE   = KEY_QUOTE, // Клавиша апострофа (').
    KEY_EQUAL        = 0xBB,      // Клавиша равенства (=).
    KEY_COMMA        = 0xBC,      // Клавиша запятой (,).
    KEY_MINUS        = 0xBD,      // Клавиша минуса (-).
    KEY_DOT          = 0xBE,      // Клавиша точки (.).
    KEY_SLASH        = 0xBF,      // Клавиша слэша (/).
    KEY_GRAVE        = 0xC0,      // Клавиша грависа (`).
    KEY_LBRACKET     = 0xDB,      // Левая квадратная скобка ([).
    KEY_PIPE         = 0xDC,      // Клавиша вертикальной черты (|).
    KEY_BACKSLASH    = KEY_PIPE,  // Клавиша обратного слэша (\).
    KEY_RBRACKET     = 0xDD,      // Правая квадратная скобка (]).

    KEY_COUNT        = 256        // Максимальное количество поддерживаемых кодов клавиш (не является реальным кодом).
} keyboard_key;
