#pragma once

#include <core/defines.h>
#include <renderer/vulkan/vulkan_types.h>

/*
    @brief Проверяет, является ли результат Vulkan успешным.
    @note Эта функция проверяет код возврата Vulkan и возвращает true,
          если операция завершилась успешно (не ошибка).
    @param result Код возврата Vulkan (VkResult).
    @return true если результат успешный, false если ошибка.
*/
bool vulkan_result_is_success(VkResult result);

/*
    @brief Получает текстовое описание кода результата Vulkan.
    @note Возвращает читаемую строку с описанием кода возврата Vulkan.
          Полезно для логирования ошибок и отладки.
    @param result Код возврата Vulkan (VkResult).
    @return Строка с описанием результата.
*/
const char* vulkan_result_get_string(VkResult result);
