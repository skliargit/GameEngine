#pragma once

// ============================================================================
// Математические константы и предельные значения
// ============================================================================

// @brief Приблизительное значение числа π (отношение длины окружности к диаметру).
#define MATH_PI             3.14159265358979323846f

// @brief Приблизительное значение числа 2π (полный оборот в радианах).
#define MATH_2PI            (2.0f * MATH_PI)

// @brief Приблизительное значение числа π/2 (прямой угол в радианах).
#define MATH_HALF_PI        (0.5f * MATH_PI)

// @brief Приблизительное значение числа π/4 (угол 45° в радианах).
#define MATH_QUARTER_PI     (0.25f * MATH_PI)

// @brief Приблизительное значение числа 1/π (полезно для оптимизации делений).
#define MATH_INV_PI         (1.0f / MATH_PI)

// @brief Приблизительное значение числа 1/2π (полезно для оптимизации делений).
#define MATH_INV_2PI        (1.0f / MATH_2PI)

// @brief Приблизительное значение квадратного корня из 2 (диагональ единичного квадрата, отношение сторон бумаги формата А4).
#define MATH_SQRT_2         1.41421356237309504880f

// @brief Приблизительное значение квадратного корня из 3 (высота равностороннего треугольника со стороной 2).
#define MATH_SQRT_3         1.73205080756887729352f

// @brief Приблизительное значение квадратного корня из 5 (используется в алгоритмах, связанных с золотым сечением).
#define MATH_SQRT_5         2.23606797749978969641f

// @brief Приблизительное значение один делить на квадратный корень из 2 (используется для нормализации, sin и cos угла 45°).
#define MATH_INV_SQRT_2     (1.0f / MATH_SQRT_2)

// @brief Приблизительное значение один делить на квадратный корень из 3.
#define MATH_INV_SQRT_3     (1.0f / MATH_SQRT_3)

// @brief Приблизительное значение один делить на квадратный корень из 5.
#define MATH_INV_SQRT_5     (1.0f / MATH_SQRT_5)

// @brief Приблизительное значение числа Эйлера (основание натурального логарифма).
#define MATH_E              2.71828182845904523536f

// @brief Приблизительное значение один делить на число Эйлера.
#define MATH_INV_E          (1.0f / MATH_E)

// @brief Приблизительное значение числа ФИ (золотое сечение).
#define MATH_PHI            ((1.0f + MATH_SQRT_5) * 0.5f)

// @brief Приблизительное значение логарифма числа e по основанию 2 (используется для преобразования между натуральными и двоичными логарифмами).
#define MATH_LOG2_E         1.44269504088896340736f

// @brief Приблизительное значение логарифма числа e по основанию 10.
#define MATH_LOG10_E        0.43429448190325182765f

// @brief Приблизительное значение натурального логарифма числа 2.
#define MATH_LN_2           0.69314718055994530942f

// @brief Приблизительное значение натурального логарифма числа 10.
#define MATH_LN_10          2.30258509299404568402f

// ============================================================================
// Коэффициенты преобразования единиц измерения
// ============================================================================

// @brief Коэффициент преобразования градусов в радианы.
#define MATH_DEG_TO_RAD     (MATH_PI / 180.0f)

// @brief Коэффициент преобразования радиан в градусы.
#define MATH_RAD_TO_DEG     (180.0f / MATH_PI)
